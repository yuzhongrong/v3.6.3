package model;

import java.io.Serializable;
import java.util.ArrayList;

public class ProfitList implements Serializable{
	private static final long serialVersionUID = -2507399553211633035L;
	private String total;
	private ArrayList<Profit>list;
	private String type;
	public String getTotal() {
		return total;
	}
	public void setTotal(String total) {
		this.total = total;
	}
	public ArrayList<Profit> getList() {
		return list;
	}
	public void setList(ArrayList<Profit> list) {
		this.list = list;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	
}
