package model;

import java.io.Serializable;
import java.util.ArrayList;

public class TransferInfoList implements Serializable {
	private static final long serialVersionUID = -8739205111864039417L;
	private String goods_type;
	private String name;
	private int count;
	private String success_total;

	public String getTransfer_money() {
		return success_total;
	}

	public void setTransfer_money(String success_total) {
		this.success_total = success_total;
	}

	public int getCount() {
		return count;
	}

	public void setCount(int count) {
		this.count = count;
	}

	private ArrayList<TransferInfo> list;

	public String getGoods_type() {
		return goods_type;
	}

	public void setGoods_type(String goods_type) {
		this.goods_type = goods_type;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public ArrayList<TransferInfo> getList() {
		return list;
	}

	public void setList(ArrayList<TransferInfo> list) {
		this.list = list;
	}

}
