package com.jinr.core.bankCard.banklist.main;

/**
 * 城市属性实体类
 * 
 * @author sy
 * 
 */
public class BankModel {
	private String BankName; // 银行名字名字
	private String NameSort; // 银行首字母
	private String BankID; // 银行的ID
	private String BankShortName; // 银行拼音缩写

	public String getBankName() {
		return BankName;
	}

	public void setBankName(String cBankName) {
		BankName = cBankName;
	}

	public String getNameSort() {
		return NameSort;
	}

	public void setNameSort(String nameSort) {
		NameSort = nameSort;
	}

	public String getBankID() {
		return BankID;
	}

	public void setBankID(String cBankID) {
		BankID = cBankID;
	}

	public String getBankShortName() {
		return BankShortName;
	}

	public void setBankShortName(String cBankShortName) {
		BankShortName = cBankShortName;
	}
}